// 绑定图片URL输入
function bindImageInputs() {
    const imgMap = {
        'edit_slide_img_1': 'slide_img_1',
        'edit_slide_img_2': 'slide_img_2',
        'edit_slide_img_3': 'slide_img_3',
        'edit_ad_img': 'ad_img'
    };

    for (const [editId, targetId] of Object.entries(imgMap)) {
        const input = document.getElementById(editId);
        if (input) {
            input.addEventListener('input', function () {
                const img = document.getElementById(targetId);
                if (img && this.value.trim()) {
                    img.src = this.value.trim();
                }
            });
        }
    }
}

// 保存所有编辑
function saveAll() {
    document.querySelectorAll('.edit-group-content input[data-target]').forEach(input => {
        const targetKey = input.dataset.target;
        const value = input.value;
        const editableElements = document.querySelectorAll(`[data-editable="${targetKey}"]`);
        editableElements.forEach(el => { el.textContent = value; });
        localStorage.setItem('edit_' + targetKey, value);
    });

    const slideFields = ['slide_1_title', 'slide_1_author', 'slide_1_date',
                         'slide_2_title', 'slide_2_author', 'slide_2_date',
                         'slide_3_title', 'slide_3_author', 'slide_3_date'];

    slideFields.forEach(field => {
        const input = document.getElementById('edit_' + field);
        if (input && input.value.trim()) {
            localStorage.setItem('edit_' + field, input.value);
        }
    });

    alert('保存成功！刷新页面后内容会保留。');
}

// 加载已保存的编辑
function loadSavedEdits() {
    document.querySelectorAll('[data-editable]').forEach(el => {
        const key = el.dataset.editable;
        const saved = localStorage.getItem('edit_' + key);
        if (saved) el.textContent = saved;
    });

    ['slide_1_title', 'slide_1_author', 'slide_1_date',
     'slide_2_title', 'slide_2_author', 'slide_2_date',
     'slide_3_title', 'slide_3_author', 'slide_3_date'].forEach(field => {
        const saved = localStorage.getItem('edit_' + field);
        if (saved) {
            const input = document.getElementById('edit_' + field);
            if (input) input.value = saved;
        }
    });

    for (let i = 1; i <= 3; i++) {
        const saved = localStorage.getItem('slide_img_' + i);
        if (saved) {
            const img = document.getElementById('slide_img_' + i);
            if (img) img.src = saved;
            const input = document.getElementById('edit_slide_img_' + i);
            if (input) input.value = saved;
        }
    }

    const savedAd = localStorage.getItem('ad_img');
    if (savedAd) {
        const img = document.getElementById('ad_img');
        if (img) img.src = savedAd;
        const input = document.getElementById('edit_ad_img');
        if (input) input.value = savedAd;
    }

    document.querySelectorAll('[data-editable]').forEach(el => {
        const key = el.dataset.editable;
        const input = document.getElementById('edit_' + key);
        if (input) input.value = el.textContent;
    });
}

// 恢复默认
function resetAll() {
    if (!confirm('确定恢复默认内容吗？所有自定义修改将被清除。')) return;
    localStorage.clear();
    location.reload();
}

// 导出HTML
function exportHTML() {
    const clone = document.documentElement.cloneNode(true);
    const editOverlay = clone.querySelector('.edit-overlay');
    if (editOverlay) editOverlay.remove();
    const mobileMenu = clone.querySelector('.mobile-menu');
    if (mobileMenu) mobileMenu.remove();
    const mobileOverlay = clone.querySelector('.mobile-menu-overlay');
    if (mobileOverlay) mobileOverlay.remove();
    const mobileFooter = clone.querySelector('.mobile-footer');
    if (mobileFooter) mobileFooter.remove();

    clone.querySelectorAll('[data-editable]').forEach(el => el.removeAttribute('data-editable'));
    clone.querySelectorAll('.edit-field').forEach(f => f.remove());
    clone.querySelectorAll('.edit-group').forEach(f => f.remove());

    const scriptTags = clone.querySelectorAll('script[src="script.js"]');
    scriptTags.forEach(s => {
        s.remove();
        const newScript = document.createElement('script');
        newScript.textContent = `
            (function(){
                var si=0,slides=document.querySelectorAll('.slide'),dots=document.querySelectorAll('.dot');
                function update(){slides.forEach(function(s,i){s.classList.toggle('active',i===si)});dots.forEach(function(d,i){d.classList.toggle('active',i===si)})}
                function next(){si=(si+1)%slides.length;update();setTimeout(next,4000)}
                if(slides.length>0){update();setTimeout(next,4000)}
            })();
        `;
        s.parentNode.appendChild(newScript);
    });

    const html = '<!DOCTYPE html>\n<html lang="zh-CN">\n' + clone.innerHTML + '\n</html>';
    const blob = new Blob([html], { type: 'text/html;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = '商品目录.html';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
}
